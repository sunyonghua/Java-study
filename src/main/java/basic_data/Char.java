package basic_data;

/**
 * @author sun yong hua
 * @description Char
 * @createDate 2019-05-20 17:03
 * char类型是一个单一的 16 位 Unicode 字符；
 * 最小值是 \u0000（即为0）；
 * 最大值是 \uffff（即为65,535）；
 * char 数据类型可以储存任何字符；
 * 例子：char letter = 'A';。
 */
public class Char {
  public static void main(String[] args) {
    char a = 'A';
    char b = 65;
    System.out.println(a);
    System.out.println(b);
  }
}
