package basic_data;

/**
 * @author sun yong hua
 * @description Double
 * @createDate 2019-05-20 16:58
 * double 数据类型是双精度、64 位、符合IEEE 754标准的浮点数；
 * 浮点数的默认类型为double类型；
 * double类型同样不能表示精确的值，如货币；
 * 默认值是 0.0d；
 * 例子：double d1 = 123.4。
 */
public class Double {
  public static void main(String[] args) {
    double d1 = 12000000000D;
    System.out.println(d1);
  }
}
